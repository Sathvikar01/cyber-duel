// NOTE FOR THE BODY STRUCTURE AGENT:
// Each character below defines a `silhouette` config that drives the unique
// shape of their body. The two fields you should use when rebuilding the
// torso as a chest+waist composite are:
//
//   silhouette.shoulderWidth  -> width of the chest (upper torso box)
//   silhouette.waistNarrow    -> 0 = no taper (chest == waist),
//                                1 = very tapered (waist << chest)
//
// Waist width should be roughly:
//   waistW = shoulderWidth * (1 - 0.55 * waistNarrow) * 0.45 * torsoScale
//
// Other silhouette fields (armLength, armThickness, legStyle, legThickness,
// build, headShape, hasTopknot, hasHood, hasMohawk, isBald, hasWrappedHands,
// hasGauntlets, hasBoots, hasShoulderPads, hasKatanaSheath, hasSash, hasScarf)
// are consumed by SilhouetteFighter.jsx and should be respected when defining
// pose data, head/limb attachment points, or collision volumes.
//
// Three optional objects further differentiate each character:
//
//   silhouette.proportions    -> per-character base multipliers applied to
//                                torsoW/H, armW/H, legW/H, headW/H/D in
//                                addition to torsoScale/limbScale/headScale.
//                                Values around 1.0 = neutral; >1.0 = bigger,
//                                <1.0 = smaller. This is what makes the
//                                four fighters read as fundamentally
//                                different body types (short+wide monk vs.
//                                tall+slim assassin, etc.).
//
//   silhouette.stanceModifier -> idle-pose offsets: torsoTilt (forward lean,
//                                radians), armForward (arms up in guard),
//                                armSpread (z-rotation of the shoulders,
//                                larger = wider guard), legSpread (z-rotation
//                                of the legs, wider = wider stance), kneeBend
//                                (calf forward bend for a crouch), headTilt
//                                (x-rotation, head down/forward).
//
//   silhouette.idleAnim       -> per-frame breathing/sway parameters:
//                                breathAmp/Rate (hipY oscillation),
//                                torsoSwayAmp/Rate (X-axis torso tilt),
//                                torsoRollAmp/Rate (Z-axis torso sway),
//                                headSwayAmp/Rate + headScanAmp/Rate (head
//                                Y-axis rotation), weightShiftAmp/Rate
//                                (Z-axis weight shift on top of the base
//                                pose), chestExpand (scale.y breath gain).

